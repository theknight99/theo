def example_util_function(param1, param2):
    """
    An example utility function that performs a simple operation.
    
    Args:
        param1: The first parameter.
        param2: The second parameter.
    
    Returns:
        The result of the operation.
    """
    return param1 + param2

def another_util_function(data):
    """
    An example utility function that processes a list of data.
    
    Args:
        data: A list of data to process.
    
    Returns:
        A list of processed data.
    """
    return [item * 2 for item in data]